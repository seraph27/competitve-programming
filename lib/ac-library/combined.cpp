void shiina_mashiro() {
    int n, m; cin >> n >> m;
    atcoder::mf_graph<int> g(n);
    vector<pii> edges;
    for(int i = 0; i < m; i++) {
        int a, b; cin >> a >> b;
        --a; --b;
        edges.emplace_back(a, b);
        g.add_edge(a, b, 1);
        g.add_edge(b, a, 1);
    }
    int ans = g.flow(0, n-1);
    auto v = g.min_cut(0);

    cout << ans << nl;
    for(auto&[fi, se] : edges) {
        if(v[fi] != v[se]) {
            cout << fi + 1 << " " << se + 1 << nl;
        } 
    }

}

