const char nl = '\n';

void shiina_mashiro() {
    int n, m; cin >> n >> m;
    vector<int> a(n), b(m);
    for(auto &i : a) cin >> i;
    for(auto &i : b) cin >> i;
    int st = a[0];
    sort(all(a));
    sort(all(b));
    int lst = a[n-1];
    vector<int> v;
    for(int i = 0; i < m; i++) {
        if(b[i] > st && b[i] <= lst) {
            int l = lower_bound(all(a), b[i]) - a.begin();
            v.emplace_back(n-l+1);
        } else {
            v.emplace_back(1);
        }
    }
    sort(all(v));
    debug(v);
    vector<int> ans(m);
    for(int i = 0; i < m; i++) {
        for(int j = i; j < m; j+=i+1) {
            ans[i] += v[j];
        }
        cout << ans[i] << " ";
    }
    cout << nl;
}

