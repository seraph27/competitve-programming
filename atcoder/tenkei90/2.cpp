void seraph() {
    int n; cin >> n;
    if(n%2) return;
    for(int i = 0; i < 1<<n; i++) {
        string build = "";
        for(int bit = n-1; bit>=0; bit--) {
            if((i>>bit)&1) build.pb(')');
            else build.pb('(');
        }
        int cnt = 0, bad = 0;
        for(int j = 0; j < n; j++) {
            cnt+=build[j]=='('? 1 : -1;
            if(cnt<0) {
                bad = 1;
                break;
            }
        }
        if(!bad&&cnt==0) cout << build << nl;
    }
}

