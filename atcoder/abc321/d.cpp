const char nl = '\n';

void shiina_mashiro() {
    int n, m; cin >> n >> m;
    int p; cin >> p;
    vector<int> a(n), b(m);
    for(auto&x: a) cin >> x;
    for(auto&x: b) cin >> x;
    sort(all(a));
    sort(all(b));
    vector<int> pref_b(m+1);
    for(int i = 0; i < m; i++) {
        pref_b[i+1] += pref_b[i] + b[i];
    }
    int ans = 0;
    for(int i = 0; i < n; i++) {
        auto pos = lower_bound(all(b), p - a[i]) - b.begin();
        auto left = m - pos;
        ans += pref_b[pos] + a[i] * pos;
        ans += p * left;
    }
    cout << ans << nl;
}

