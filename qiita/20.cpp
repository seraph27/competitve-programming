void shiina_mashiro() {
    int n; cin >> n;
    vector<int> top(n), mid(n), bot(n);
    for(auto&a: top) cin >> a;
    for(auto&a: mid) cin >> a;
    for(auto&a: bot) cin >> a;
    sort(all(top));
    sort(all(mid));
    sort(all(bot));
    ll ans = 0;
    for(int i = 0; i < n; i++) {
        auto left = lower_bound(all(top), mid[i]) - top.begin();
        auto right = upper_bound(all(bot), mid[i]) - bot.begin();
        ans += (ll)left*(n-right);
    }
    cout<<ans<<nl;
}

