void shiina_mashiro() {
    while(true) {
        int n, x; cin >> n >> x;
        int ans = 0;
        if(n==0 && x==0) return;
        for(int i = 1; i <= n; i++) for(int j = i+1; j <= n; j++) for(int k = j+1; k <= n; k++) {
            if(i+j+k == x) ans++;
        }
        cout << ans << nl;
    }
}

